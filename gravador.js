// ÁUDIO DO GRAVADOR
const audio = new Audio("tape.mp3");
audio.loop = true; // para o som rodar continuamente enquanto a fita passa
audio.volume = 0.7; 

const gravadorFundo = document.getElementById("gravador-fundo");
const caixaFita = document.getElementById("caixa-fita");

function playAudio() {
  audioToggle.textContent = "ÁUDIO: ON";
  audio.currentTime = 0;
  audio.play().catch(e => console.log("Autoplay bloqueado:", e));
}

function stopAudio() {
  audioToggle.textContent = "ÁUDIO: OFF";
  audio.pause();
  audio.currentTime = 0;
}


// ===========================
//   VARIÁVEIS DE CONTROLE
// ===========================
let fitaAtual = null;        // ID da fita escolhida
let captionText = "";        // Texto completo
let isPlaying = false;       // Está tocando?
let isPossiblePlay = false;  // Controle de fim
let currentPos = 10;
let captionOffset = 0;

const captionDisplay = document.getElementById("caption-display");
const gravador = document.getElementById("gravador-topo");

// ===========================
//   FUNÇÃO PRINCIPAL
// ===========================
function updateCaption() {
  if (!isPlaying) return;

  captionOffset -= 1;
  let visibleText = '';
  currentPos = 10;

  for (let i = 0; i < captionText.length; i++) {
    const char = captionText[i];
    if (currentPos >= 10 && currentPos < (34 + 10)) {
      visibleText += char;
    }
    currentPos += 1;
  }

  if (visibleText === "...[ EOF ]...") {
    stopCaption();
    isPossiblePlay = false;
	stopAudio();
	gravadorFundo.src = `img/fita${fitaAtual}Stop.gif`;
  }

  captionText = captionText.slice(1);
  captionDisplay.textContent = visibleText.padEnd(34, ' ');
}

setInterval(updateCaption, 130);

// ===========================
//   PARAR LEITURA
// ===========================
function stopCaption() {
  isPlaying = false;
  captionDisplay.textContent = "";
  stopAudio();
}

// ===========================
//  CLICAR EM UMA FITA
// ===========================
document.querySelectorAll(".fita").forEach(fita => {
  fita.addEventListener("click", () => {
	const id = fita.dataset.id;
	
	// Mostrar caixa da fita
	caixaFita.src = `img/caixaFita${id}.png`;
	caixaFita.style.display = "block";

    gravadorFundo.src = `img/fita${id}Play.gif`;

    // Se já existe uma fita tocando, devolve para a mesa
    if (fitaAtual !== null && fitaAtual != id) {
      document.querySelector(`.fita[data-id="${fitaAtual}"]`).style.display = "block";
    }

    // Guarda a fita atual
    fitaAtual = id;

    // Some da mesa
    fita.style.display = "none";

    // Inicia texto
	captionText = fitaTextos[id] + "...[ EOF ]...";
    isPlaying = true;
    isPossiblePlay = true;
	
	 playAudio();
  });
});

// ===========================
//   CLICAR NO GRAVADOR
//   → devolve fita & para som
// ===========================
gravador.addEventListener("click", () => {
  if (fitaAtual !== null) {
	document.getElementById("caixa-fita").style.display = "none";
    // devolve fita para a mesa
    document.querySelector(`.fita[data-id="${fitaAtual}"]`).style.display = "block";
    fitaAtual = null;
	gravadorFundo.src = `img/gravadorSemFita.png`;
  }

  // para texto
  stopCaption();
  stopAudio();
});


